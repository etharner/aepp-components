<template>
    <li class="ae-list-item" :class="{ [fill]: Boolean(fill) }">
      <slot />
    </li>
</template>
<script>
export default {
  name: 'ae-list-item',
  props: {
    /**
     * Fill property changes the color of the borders.
     * Select something between: `neutral, default, emphasis, alternative`
     * fallback: `neutral`
     */
    fill: {
      type: String,
      default: 'neutral',
      validator: value => [
        'neutral',
        'default',
        'alternative',
        'emphasis',
      ].includes(value),
    },
  },
};
</script>
